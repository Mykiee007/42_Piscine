#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		str++;
		i++;
	}
	return (i);
}

int	main(void)
{
	int		length;
	length = ft_strlen("Patience");
	printf("%d", length);
	return (0);
}
